func calcAge(now time.Time, date time.Time) int {
	bd := time.Date(
		date.Year(),
		date.Month(),
		date.Day(),
		0,
		0,
		0,
		0,
		time.UTC,
	)

	n := time.Date(now.Year(), now.Month(), now.Day(), 0, 0, 0, 0, time.UTC)

	age := n.Year() - bd.Year()
	if n.Month() < bd.Month() || (n.Month() == bd.Month() && n.Day() < bd.Day()) {
		age--
	}

	return age
}

func convertRawToSten(
	raw int,
	traitID uint64,
	candidateGender crmEntity.CandidateGender,
	candidateBirthday *time.Time,
) int {
	age := 30
	if candidateBirthday != nil {
		age = calcAge(time.Now(), *candidateBirthday)
	}

	if candidateGender == crmEntity.CandidateGenderUnspecified {
		candidateGender = crmEntity.CandidateGenderMale
	}

	table, ok := lo.Find(normalizeTables, func(item normalizeTable) bool {
		return item.gender == candidateGender && item.ageFrom <= age && item.ageTo >= age
	})
	if !ok {
		return -1
	}

	convertMap, ok := table.convertMap[traitID]
	if !ok {
		return -1
	}

	stenValue, ok := lo.Find(convertMap, func(item normalizeTableConvertMap) bool {
		return item.rowFrom <= raw && item.rowTo >= raw
	})
	if !ok {
		return -1
	}

	return stenValue.sten
}
